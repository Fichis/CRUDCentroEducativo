    private void jtCursosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtCursosMouseClicked
        // TODO add your handling code here:
        int rowindex = jtCursos.getSelectedRow();
        txtInicio.setText(jtCursos.getModel().getValueAt(rowindex, 1).toString());
        txtFin.setText(jtCursos.getModel().getValueAt(rowindex, 2).toString());
        txtDescripcion.setText(jtCursos.getModel().getValueAt(rowindex, 3).toString());
        
    }//GEN-LAST:event_jtCursosMouseClicked

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        int rowindex = jtCursos.getSelectedRow();
        String id = jtCursos.getModel().getValueAt(rowindex, 0).toString();
        
        CursoAcademicoDAO_IMP cursoaca = CursoAcademicoDAO_IMP.getInstance();
        CursoAcademico c = getCampos();
        c.setId(Integer.parseInt(id));
        try {
            cursoaca.update(c);
            JOptionPane.showMessageDialog(this, "Curso academico EDITADO correctamente");
            muestraTable();
        } catch (SQLException ex) {
            Logger.getLogger(jfrCursoAcademico.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        // TODO add your handling code here:
        int rowindex = jtCursos.getSelectedRow();
        String id = jtCursos.getModel().getValueAt(rowindex, 0).toString();
        
        CursoAcademicoDAO_IMP cursoaca = CursoAcademicoDAO_IMP.getInstance();
        CursoAcademico c = getCampos();
        c.setId(Integer.parseInt(id));
        try {
            cursoaca.delete(c.getId());
            JOptionPane.showMessageDialog(this, "Curso academico ELIMINADO correctamente");
            muestraTable();
        } catch (SQLException ex) {
            Logger.getLogger(jfrCursoAcademico.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnBorrarActionPerformed

    private CursoAcademico getCampos(){
        CursoAcademico c=new CursoAcademico();
        c.setYearinicio(Integer.parseInt(txtInicio.getText()));
        c.setYearfin(Integer.parseInt(txtFin.getText()));
        c.setDescripcion(txtDescripcion.getText());
        return c;        
    }
    
