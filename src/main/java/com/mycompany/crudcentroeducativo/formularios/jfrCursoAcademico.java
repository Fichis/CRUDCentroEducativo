    private void muestraTable(){
        DefaultTableModel modelo = (DefaultTableModel)jtCursos.getModel();
        modelo.setNumRows(0);
        
        CursoAcademicoDAO_IMP cursoaca = CursoAcademicoDAO_IMP.getInstance();
        String[] fila = new String[4];
        
        try {
            ArrayList<CursoAcademico> lista = cursoaca.getAll();
            
            for(CursoAcademico curso : lista){
                fila[0]=""+curso.getId();
                fila[1]=""+curso.getYearinicio();
                fila[2]=""+curso.getYearfin();
                fila[3]=curso.getDescripcion();
                
                modelo.addRow(fila);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(jfrCursoAcademico.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }
    
